/**
 * Javascript have 3 type of function
 * 1. Name function
 * 2. Anonymous function
 * 3. Arrow function (ES6)
 * */ 

// Name function
function funcName(){
    console.log('function name!');
}
funcName();

// Anonymous function
let funcAnony = function(){
    console.log('function anonymous!');
}
funcAnony();

// Arrow function
const funcArrow = () => {
    console.log('Arrow function!');
}
funcArrow();