/* I.Javascripts have 3 type of variable: */
// 1. Var
var name = 'Tiennx';
console.log(name);
// output : Tiennx

// ES6 (let vs const được định nghĩa từ phiên bản ES6 của javascript)
// 2. Let
let  firstName = 'Tien';
console.log(firstName);
// output: Tien

// 3. Const
const lastName = 'NguyenXuan';
console.log(lastName);
//output: NguyenXuan
